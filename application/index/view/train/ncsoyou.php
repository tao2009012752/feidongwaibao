							<p class="biaoti">(一)	服务外包发展前景巨大，从业人员多，需求量大</p>
							<p class="duan">截至2012年底，我国共有服务外包企业21159家，从业人员428.9万人，未来5年，中国在服务外包人才数量上的缺口高达300万人。而如果算上在岸外包，那从业人员以及缺口将十分庞大。</p>
							<p class="biaoti">(二)	唯一性</p>
							<p class="duan">目前，NCSO是中国服务外包领域唯一的服务外包应用型人才的职业能力水平测评体系，市场前景巨大。</p>
							<p class="biaoti">(三)	国家政策支持</p>
							<p class="duan">财政部、商务部《关于做好2013年度承接国际服务外包业务发展资金申报管理工作的通知》（财企[2013]52号）中明确指出：服务外包培训机构培训的从事服务外包业务人才（大学以上学历），通过服务外包业务专业知识和技能培训考核，并与服务外包企业签订1年以上劳动合同的，给予培训机构每人不超过500元的定额培训支持。同时，原则上要求地方政府按照不低于1:1的配套资金给予支持。</p>
							<p class="biaoti">(三)	国家政策支持</p>
							<p class="duan">《国务院办公厅关于进一步促进服务外包产业发展的复函》（国办函〔2013〕33号）中也指出：中央财政继续对地方安排补助资金，对符合条件的企业、单位开展服务外包人才培训、资质认证等给予补助。并且不断完善服务外包中高端人才鼓励政策，对中高端人才培训加大补助力度。重点支持一批办学规范、实力强、效果好的高等学校和社会培训机构。</p>
							<p class="duan">注：以上政策仅供参考，具体补贴政策可向当地商务部门咨询。</p>
